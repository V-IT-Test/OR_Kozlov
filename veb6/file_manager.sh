dir_name="my_directory"

# Создание директории
mkdir "$dir_name"

# Переход в новую директорию
cd "$dir_name" || { echo "Не удалось перейти в директорию $dir_name"; exit 1; }

# Создание нескольких файлов
touch file1.txt file2.txt file3.txt

# Вывод списка созданных файлов
echo "Созданы файлы:"
ls

# Удаление созданных файлов
rm file1.txt file2.txt file3.txt

# Вывод списка файлов после удаления
echo "Файлы после удаления:"
ls

# Переход обратно в исходную директорию
cd ..

# Удаление директории
rmdir "$dir_name"

echo "Директория '$dir_name' удалена."